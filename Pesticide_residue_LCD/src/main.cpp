#include <Wire.h>
#include <ST7032.h>
#include <Arduino.h>

ST7032 lcd;

void setup() {
  //LCD表示領域設定(8桁, 2行)
  lcd.begin(8, 2);

  //コントラスト設定(0〜63)
  lcd.setContrast(30);

}

void loop() {
  //LCD表示(1行目)
  lcd.setCursor(0, 0);
  lcd.print("test");

  // LCD表示(2行目)
  lcd.setCursor(0, 1);
  lcd.print("test");
}